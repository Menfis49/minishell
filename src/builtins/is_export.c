#include "minishell.h"

int	is_export(char *s)
{
	if (ft_strlen(s) == 6)
	{
		if (s[0] == 'e' && s[1] == 'x' && s[2] == 'p' \
			&& s[3] == 'o' && s[4] == 'r' && s[5] == 't')
			return (1);
	}
	return (0);
}
